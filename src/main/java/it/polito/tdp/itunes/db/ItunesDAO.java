	public List<Album> getAllAlbumsBYDurata(double durata){
		final String sql = "SELECT t.AlbumId, a.Title, SUM(t.Milliseconds/1000/60) AS durata, COUNT(*) AS numTracce "
				+ "FROM track t, album a "
				+ "WHERE t.AlbumId = a.AlbumId "
				+ "GROUP BY t.AlbumId "
				+ "HAVING SUM(t.Milliseconds/1000/60) > ? ";
		List<Album> result = new LinkedList<>();
		
		try {
			Connection conn = DBConnect.getConnection();
			PreparedStatement st = conn.prepareStatement(sql);
			st.setDouble(1, durata); 
			ResultSet res = st.executeQuery();

			while (res.next()) {
				result.add(new Album(res.getInt("AlbumId"), res.getString("Title"), res.getDouble("durata")));
			}
			conn.close();
		} catch (SQLException e) {
			e.printStackTrace();
			throw new RuntimeException("SQL Error");
		}
		return result;
	}
	
	public List<Arco> getArchi(Map<Integer, Album> mappa ){
		
		String sql = "SELECT DISTINCT t1.AlbumId AS a1, t2.AlbumId AS a2 "
				+ "FROM  (SELECT  p.PlaylistId, p.TrackId, t.AlbumId "
				+ "		 FROM playlisttrack p, track t "
				+ "		 WHERE p.TrackId = t.TrackId ) AS t1,  "
				+ "		 (SELECT  p.PlaylistId, p.TrackId, t.AlbumId "
				+ "		 FROM playlisttrack p, track t "
				+ "		 WHERE p.TrackId = t.TrackId ) AS t2 "
				+ "WHERE t1.PlaylistId = t2.PlaylistId AND t1.TrackId > t2.TrackId AND t1.AlbumId > t2.AlbumId "; 
		List<Arco> result = new LinkedList<>();
		
		try {
			Connection conn = DBConnect.getConnection();
			PreparedStatement st = conn.prepareStatement(sql);
			ResultSet res = st.executeQuery();

			while (res.next()) {
				Album a1 = mappa.get(res.getInt("a1")); 
				Album a2 = mappa.get(res.getInt("a2")); 
				result.add(new Arco(a1, a2)); 
				
			}
			conn.close();
		} catch (SQLException e) {
			e.printStackTrace();
			throw new RuntimeException("SQL Error");
		}
		return result;
		
		
	}
